public class Oper3 {
    public static void main(String[] args) {

        /* Algoritma */

        if (true && true) {
            System.out.println("true && true = " + (true && true));
        }

        if (true & true) {
            System.out.println("true & true = " + (true & true));
        }

        if (true) {
            System.out.println("true = " + true);
        }

        if (true || true) {
            System.out.println("true || true = " + (true || true));
        }

        if (true | false) {
            System.out.println("true | false = " + (true | false));
        }
    }
}